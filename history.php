    <h2>History of University of Iowa Anti-Violence Work</h2>
<p>The University of Iowa has a long history of supporting anti-violence work on campus and in the Iowa City area. Our successes have been driven by multidisciplinary collaboration and the continuous effort of a committed team.</p>

<p>Efforts go back to the establishment of the Rape Victim Advocacy Center at the Women’s Center in 1971, as well as the grant Aid and Alternatives to Victims of Spouse Abuse received in 1979 for a shelter that would eventually become part of the Domestic Violence Intervention Program (DVIP). These programs, now independent agencies in the Iowa City community, continue to play a major role in our coordinated campus response.</p>

<p>The timeline below describes campus work to prevent sexual misconduct, dating violence, and stalking; provide support to survivors; and hold offenders accountable since the creation of the UI Anti-Violence Coalition. Much has been accomplished, but experience, a trauma-informed approach, and survivors continue to inform practice—and there is more work to be done, including the introduction of the UI’s new Anti-Violence Plan for sexual misconduct, dating violence, and stalking for Summer 2018 to Spring 2021.</p>
